package com.titos.lystra.ui.theme

import androidx.compose.ui.unit.dp

/**
 * Spacing & dimension constants matching the design system.
 * Based on an 8dp baseline grid.
 */
object Dimens {
    /** Base unit for the 8dp grid */
    val Base = 8.dp

    /** Minimum touch target size (48dp) for accessible tap areas */
    val TouchTarget = 48.dp

    /** Screen edge margin (16dp) */
    val MarginEdge = 16.dp

    /** Spacing between inline elements (12dp) */
    val Gutter = 12.dp

    /** Standard list item height (64dp) */
    val ListItemHeight = 64.dp

    /** Bottom navigation bar height (80dp) */
    val BottomNavHeight = 80.dp

    /** Top app bar height (56dp) */
    val TopBarHeight = 56.dp

    /** FAB size (56dp) */
    val FABSize = 56.dp

    /** Minimum bottom padding to avoid FAB overlap (80dp) */
    val FABClearance = 80.dp

    /** Category icon size in history list (48dp) */
    val CategoryIconSize = 48.dp

    /** Product header icon size in edit screen (96dp) */
    val ProductIconSize = 96.dp

    /** Quantity button size (64dp) */
    val QuantityButtonSize = 64.dp
}
